export function weatherCodeToLabel(code: number) {
  return WEATHER_CODES[code] ?? "Unclassified weather";
}

export function formatTemperature(value: number) {
  return `${value.toFixed(1)}°C`;
}

export function formatWind(speedKph: number, directionDeg: number) {
  return `${speedKph.toFixed(1)} km/h at ${directionDeg}°`;
}

export function formatObservationTime(timestamp: string) {
  return new Intl.DateTimeFormat("en-US", {
    month: "short",
    day: "numeric",
    hour: "numeric",
    minute: "2-digit",
    timeZone: "UTC",
    timeZoneName: "short",
  }).format(new Date(timestamp));
}
